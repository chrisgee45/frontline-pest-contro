test('payment auto-posts to transactions feed AND accounting ledger', async () => {
  const inv = await makeInvoiceWithBalance(150);

  // Snapshot before
  const txnsBefore = (await request(server, 'GET', '/api/admin/transactions', { token })).body.transactions;
  const jeBefore = (await request(server, 'GET', '/api/accounting/journal-entries', { token })).body.data || [];

  await request(server, 'POST', `/api/admin/invoices/${inv.id}/payments`, {
    token,
    body: { amount: inv.total, paymentMethod: 'cash' },
  });

  const txnsAfter = (await request(server, 'GET', '/api/admin/transactions', { token })).body.transactions;
  const jeAfter = (await request(server, 'GET', '/api/accounting/journal-entries', { token })).body.data || [];

  // One new income transaction
  const newTxns = txnsAfter.filter(t => !txnsBefore.some(b => b.id === t.id));
  assert.equal(newTxns.length, 1);
  assert.equal(newTxns[0].type, 'income');
  assert.equal(newTxns[0].amount, inv.total);

  // At least one new journal entry (the revenue entry: Dr Cash / Cr Revenue)
  const newJE = jeAfter.filter(e => !jeBefore.some(b => b.id === e.id));
  assert.ok(newJE.length >= 1, 'expected a journal entry for the payment');
  // Entry must balance
  const ent = newJE[0];
  const debits = ent.lines.reduce((s, l) => s + Number(l.debit), 0);
  const credits = ent.lines.reduce((s, l) => s + Number(l.credit), 0);
  assert.ok(Math.abs(debits - credits) < 0.01);
  assert.ok(Math.abs(debits - inv.total) < 0.01);
});

test('DELETE /api/admin/payments/:id reverses BOTH the transaction and the journal entry (no silent orphaning)', async () => {
  const inv = await makeInvoiceWithBalance(180);

  const payRes = await request(server, 'POST', `/api/admin/invoices/${inv.id}/payments`, {
    token,
    body: { amount: inv.total, paymentMethod: 'cash' },
  });
  const paymentId = payRes.body.payment.id;
  const journalEntryId = payRes.body.payment.journalEntryId;
  assert.ok(journalEntryId, 'payment should have been linked to a journal entry');

  // Snapshot after the payment was recorded
  const incomeBefore = (await request(server, 'GET', '/api/admin/transactions', { token }))
    .body.transactions
    .filter(t => t.type === 'income' && !t.reversalOf)
    .reduce((s, t) => s + Number(t.amount), 0);

  // Void the payment
  const del = await request(server, 'DELETE', `/api/admin/payments/${paymentId}`, { token });
  assert.equal(del.status, 200);
  assert.equal(del.body.invoice.effectiveStatus, 'draft', 'invoice with no payments should be back to draft');
  assert.equal(del.body.invoice.paidAmount, 0);
  assert.equal(del.body.invoice.balance, inv.total);

  // The reversal transaction is a NEW row with negative amount — original
  // is still present (audit integrity), but net sum is zero.
  const txnsAfter = (await request(server, 'GET', '/api/admin/transactions', { token })).body.transactions;
  const reversal = txnsAfter.find(t => t.reversalOf && /REVERSAL/.test(t.description || ''));
  assert.ok(reversal, 'a reversal transaction should have been added');
  assert.ok(reversal.amount < 0, 'reversal amount must be negative');
  assert.equal(-reversal.amount, inv.total);

  // The journal entry was voided AND a reversing entry exists in accounting.
  const jeRes = await request(server, 'GET', '/api/accounting/journal-entries', { token });
  const entries = jeRes.body.data || [];
  const original = entries.find(e => e.id === journalEntryId);
  assert.ok(original, 'original entry still present (not deleted)');
  assert.equal(original.isVoid, true, 'original entry should be marked isVoid=true');
  const reversingEntry = entries.find(e => e.sourceType === 'reversal' && e.sourceId === journalEntryId);
  assert.ok(reversingEntry, 'a reversing journal entry should exist');
  // Reversing entry must balance (and flip debits/credits of the original)
  const d = reversingEntry.lines.reduce((s, l) => s + Number(l.debit), 0);
  const c = reversingEntry.lines.reduce((s, l) => s + Number(l.credit), 0);
  assert.ok(Math.abs(d - c) < 0.01);
});

test('legacy PATCH status=paid self-heals: auto-creates a payment for the balance', async () => {
  const inv = await makeInvoiceWithBalance(75);

  // Old client still sends PATCH status=paid — should not be rejected,
  // should instead auto-record a payment for the full balance.
  const patchRes = await request(server, 'PATCH', `/api/admin/invoices/${inv.id}`, {
    token,
    body: { status: 'paid' },
  });
  assert.equal(patchRes.status, 200);
  assert.ok(patchRes.body.synthesizedPayment, 'legacy PATCH should synthesize a payment');
  assert.equal(patchRes.body.invoice.effectiveStatus, 'paid');
  assert.equal(patchRes.body.invoice.balance, 0);
});

