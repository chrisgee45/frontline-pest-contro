test('overdue is derived from past due date on a sent invoice', async () => {
  const inv = await makeInvoiceWithBalance(90);

  // Mark sent and set a due date in the past
  await request(server, 'PATCH', `/api/admin/invoices/${inv.id}`, {
    token,
    body: { status: 'sent' },
  });
  // Directly edit the stored dueDate via the generic PATCH notes... actually
  // the current PATCH handler only accepts notes and status — dueDate can't
  // be PATCHed. Instead we just verify the derivation logic: if dueDate is
  // past and payments < total, effectiveStatus is 'overdue'.
  // Hack: read the invoice, backdate its dueDate via a fresh POST.
  // Simpler: create another invoice with a past due date.
  const past = new Date();
  past.setDate(past.getDate() - 10);
  const pastDue = past.toISOString().slice(0, 10);

  const create = await request(server, 'POST', '/api/admin/invoices', {
    token,
    body: {
      customerName: 'Overdue Tester',
      items: [{ description: 'Service', quantity: 1, rate: 100 }],
      dueDate: pastDue,
    },
  });
  await request(server, 'PATCH', `/api/admin/invoices/${create.body.invoice.id}`, {
    token,
    body: { status: 'sent' },
  });
  const fetched = await getInvoice(create.body.invoice.id);
  assert.equal(fetched.effectiveStatus, 'overdue');
});

test('GET /api/admin/invoices/:id/payments returns the payment history', async () => {
  const inv = await makeInvoiceWithBalance(200);
  await request(server, 'POST', `/api/admin/invoices/${inv.id}/payments`, {
    token,
    body: { amount: 50, paymentMethod: 'cash' },
  });
  await request(server, 'POST', `/api/admin/invoices/${inv.id}/payments`, {
    token,
    body: { amount: 30, paymentMethod: 'check' },
  });

  const history = await request(server, 'GET', `/api/admin/invoices/${inv.id}/payments`, { token });
  assert.equal(history.status, 200);
  assert.equal(history.body.payments.length, 2);
  // Payment methods should be preserved
  const methods = history.body.payments.map(p => p.paymentMethod).sort();
  assert.deepEqual(methods, ['cash', 'check']);
});

